#include "./include/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (lst == NULL || del == NULL)
		return ;
	while (*lst != NULL)
	{
		temp = lst[0];
		(*del)(lst[0]->content);
		*lst = lst[0]->next;
		free(temp);
	}
	*lst = NULL;
}
